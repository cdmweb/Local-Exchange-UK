<p>An American businessman was at the pier of a small coastal Mexican village when a small boat with just one fisherman docked. Inside the small boat were several large yellowfin tuna.</p>

<p>The American complimented the Mexican on the quality of his fish and asked how long it took to catch them. The Mexican replied, "only a little while." The American then asked why didn’t he stay out longer and catch more fish? The Mexican said he had enough to support his family’s immediate needs.</p>

<p>The American then asked, "But what do you do with the rest of your time?" The Mexican fisherman said, "I sleep late, fish a little, play with my children, take siesta with my wife, Maria, stroll into the village each evening where I sip wine and play guitar with my amigos. I have a full and busy life, senor."</p>

<p>The American scoffed, "I am a Harvard MBA and could help you. You should spend more time fishing and with the proceeds buy a bigger boat, with the proceeds from the bigger boat you could buy several boats, eventually you would have a fleet of fishing boats. Instead of selling your catch to a middleman you would sell directly to the processor, eventually opening your own cannery. You would control the product, processing and distribution. You would need to leave this small coastal fishing village and move to Mexico City, then LA and eventually NYC where you will run your expanding enterprise."</p>

<p>The Mexican fisherman asked, "But senor, how long will this all take?"</p>

<p>To which the American replied, "15 or 20 years."</p>

<p>"But what then, senor?"</p>

<p>The American laughed and said, "that’s the best part. When the time is right you would announce an IPO and sell your company stock to the public and become very rich, you would make millions."</p>

<p>"Millions, senor? Then what?"</p>

<p>The American said, "Then you would retire. Move to a small coastal village where you would sleep late, fish a little, play with your kids, take siesta with your wife, stroll to the village in the evenings where you could sip wine and play your guitar with your amigos."</p>

