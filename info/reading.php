<h2><center>Alternative Economics</center></h2>

<p><b>The Grip of Death</b> - <cite>Michael Rowbotham [1998]</cite><br>
<blockquote>A study of modern money, debt slavery and destructive economics. One of the best books ever written about what is wrong with our present money system. <strong>If you never read anything else on alternative economics, read this!</strong></blockquote></p>

<p><b>The Future of Money</b> - <cite>Bernard Lietaer [2001]</cite><br>
<blockquote>Creating new wealth, work and a wiser world. A great introduction to local currencies worldwide. This book provides a road map for the monetary journey out of a decaying Industrial Age to an Information Age, where greater opportunities become available. It is also a discovery guide to the money system - the way money is created and managed in our society. (This book, published in 2001, is difficult to get hold of because it was originally scheduled to be published USA, but never was. Perhaps Bernard hit too close to the truth in his observations about the present money system we live within.)</blockquote></p>

<p><b>The Soul of Money</b> - <cite>Lynne Twist [2003]</cite><br>
<blockquote>Transforming your relationship with money and life. This compelling and funamentally liberating book shows us that examining our attitude towards money - earning it, spending it, and giving it away - can offer surprising insight into our lives, our values, and the essence of prosperity.</blockquote></p>

<p><b>Your Money or Your Life</b> - <cite>Joe Dominguez and Vicki Robin [1992]</cite><br>
<blockquote>Transforming your relationship with money and achieving financial independence. A classic that has transformed many people's lives.</blockquote></p>

<p><b>Interest and Inflation Free Money</b> - <cite>Margrit Kennedy [1995]</cite><br>
<blockquote>Creating an exchange medium that works for everybody and protects the earth. Interest free money! Think about that.</blockquote></p>

<p><b>LETS Work</b> - <cite>Peter Lang [1994]</cite><br>
<blockquote>Rebuilding the local economy. An introduction to LETS in the UK.</blockquote></p>

<p><b>Time Dollars</b> - <cite>Edgar Cahn & Jonathan Rowe [1992]</cite><br>
<blockquote>The new currency that enables Americans to turn their hidden resource - time - into personal security & community renewal. This is what Fourth Corner Exchange is about.</blockquote></p>

<p><b>The Growth Illusion</b> - <cite>Richard Douthwaite [1992]</cite><br>
<blockquote>How economic growth has enriched the few, impoverished the many and endangered the planet.</blockquote></p>

<p><b>Short Circuit</b> - <cite>Richard Douthwaite [1997]</cite><br>
<blockquote>Strengthening Local Economies for Security in an Unstable World. Douthwaite is an economist living in Ireland who has thoroughly researched the role of the global market in the dissolution of local communities as well as the role of local currencies in strengthening community. In this book he combines a scholarly and critical review of classical and alternative economics with a "how to" approach to setting up a local currency system. This is an excellent book for those interested in a "thought piece" on modern life as well as those who are seriously interested in exploring and creating alternative economies.</blockquote></p>

<p><b>Secrets of the Temple</b> - <cite>William Greider [1987]</cite><br>
<blockquote>How the Federal Reserve runs the country.</blockquote></p>

